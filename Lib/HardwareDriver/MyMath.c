float MYMATH_Sin(float ang)
{
	float value = 0;
	//ȷ��0<=ang<=360;
	while(ang < 0)
		ang += 360;
	while(ang > 360)
		ang -= 360;
	
	if(ang <= 90)				value = argTab[(int)(ang * 2)];
	else if(ang <= 180)	value = argTab[(int)((180 - ang) * 2)];
	else if(ang <= 270)	value = -argTab[(int)((ang - 180) * 2)];
	else								value = -argTab[(int)((360 - ang) * 2)];
	
	return value;
}
